// метод закрашивания области внутри

void FillImage(int row, int col)    //в качестве аргументов позиция строки и пикселя, с которого начинается раскраска
{
    if (pic[row, col] == 0)    // условие для текущего пикселя
    {
        pic[row, col] = 1;     // закрашивание единицей

        FillImage(row - 1, col);    //движение влево

        FillImage(row, col - 1);    //движение вверх

        FillImage(row + 1, col);    //движение вниз

        FillImage(row, col + 1);    //движение вправо
    }
}

